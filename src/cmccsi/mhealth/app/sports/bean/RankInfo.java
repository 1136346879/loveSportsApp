package cmccsi.mhealth.app.sports.bean;

public class RankInfo {
	public String name = "";
	public int rank = 0;
	public String group = "";
	public int value=0;
	public String unit="";
	public int wcl=0;
	public int step=0;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getRank() {
		return rank;
	}
	public void setRank(int rank) {
		this.rank = rank;
	}
	public String getGroup() {
		return group;
	}
	public void setGroup(String group) {
		this.group = group;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	public int getWcl() {
		return wcl;
	}
	public void setWcl(int wcl) {
		this.wcl = wcl;
	}
	public int getStep() {
		return step;
	}
	public void setStep(int step) {
		this.step = step;
	}

	
}
